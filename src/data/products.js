function specs(materiales, dimensiones) {
  return [
    {
      title: "Materiales",
      text: materiales,
      icon: materialesIcon,
    },
    {
      title: "Dimensiones",
      text: dimensiones,
      icon: dimensionesIcon,
    },
  ];
}

function createProduct({
  id,
  name,
  category,
  material,
  decade,
  price,
  slug,
  tag,
  description,
  materialText,
  dimensionsText,
}) {
  return {
    id,
    name,
    category,
    material,
    decade,
    price,
    precio: price,
    image: mainImage(slug),
    images: gallery(slug),
    tag,
    colors: palettes[decade],
    description,
    specs: specs(materialText, dimensionsText),
  };
}

